/* Using let */
let a = 10; // Declare a with 10
console.log(a);
a = 20; // Reassign a to 20
console.log(a);
// let a = 30; // It will be error because a has been already declared

/* Using var */
var b = 15; // Declare b with 15
console.log(b);
b = 25; // Reassign b with 25
console.log(b);
var b = 30; // Reassign b with 30
console.log(b);
