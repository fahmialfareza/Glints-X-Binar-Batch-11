// Global varible
let message = "Hello, World!";

// Function hello()
function hello() {
  let message = "Hello!";
  console.log(message);
}

// Function world()
function world() {
  let message = "World!";
  console.log(message);
}

hello(); // Call hello function
world(); // Call world function

console.log(message); // print message (global)
