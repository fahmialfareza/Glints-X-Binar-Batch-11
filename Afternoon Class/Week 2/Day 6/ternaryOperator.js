/* Ternary is a alternative of if else */
let a = 10; // Declare a
const isTodayRaining = true;

let b = a == 10 ? "Yes, it's 10" : "No, It's not 10";
console.log(b);

let c = isTodayRaining ? "Yes, it's raining" : "No, It's not raining";
console.log(c);

isTodayRaining ? console.log("Yes") : console.log("No");
