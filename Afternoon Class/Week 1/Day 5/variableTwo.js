/* Using let */
let a = 15; // Declare with value
console.log(a);

// Or
let b; // Declare
b = 20; // Assign value to b
console.log(b);

/* Using var */
var c = 12; // Declare with value
console.log(c);

// Or
var d; // Declare
d = 11; // Assign value to d
console.log(d);

/* Using const */
const z = 17; // The only way to declare const
console.log(z);
